def load_data():
    if not os.path.exists(FILE_NAME):
        return {"records": [], "reviews": []}
    with open(FILE_NAME, "r", encoding="utf-8") as f:
        try:
            return json.load(f)
        except:
            return {"records": [], "reviews": []}

def save_data(data):
    with open(FILE_NAME, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

